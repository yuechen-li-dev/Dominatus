using System.Globalization;
using Dominatus.RTSBenchmark.Simulation;

namespace Dominatus.RTSBenchmark;

public static class BattleReport
{
    public static void Write(TextWriter output, RtsBenchmarkResult result)
    {
        output.WriteLine("Dominatus.RTSBenchmark");
        output.WriteLine($"Mode: {result.Mode}");
        output.WriteLine($"Runtime: {result.EnvironmentInfo.FrameworkDescription}, {result.EnvironmentInfo.ProcessArchitecture}, {result.EnvironmentInfo.RuntimeIdentifier}");
        output.WriteLine($"OS: {result.EnvironmentInfo.OSDescription}");
        output.WriteLine($"Processors: {result.EnvironmentInfo.ProcessorCount}");
        output.WriteLine($"NativeAOT indicator: {(result.EnvironmentInfo.IsNativeAot ? "yes" : "no")}");
        output.WriteLine("Measured loop excludes rendering/GPU/windowing, network, and live model inference.");
        output.WriteLine($"Execution mode: {result.ExecutionMode} (parallel agents: {(result.ParallelAgents ? "yes" : "no")}, max degree {result.MaxDegreeOfParallelism}, workers used {result.ParallelWorkersUsed})");
        output.WriteLine($"Ticks simulated: {result.TicksSimulated}");
        output.WriteLine($"Ships: initial {result.InitialShips}, final {result.FinalShips}, destroyed {result.DestroyedShips}");
        output.WriteLine($"Winner: {result.Winner?.ToString() ?? "Draw"}");
        output.WriteLine($"Fleet power: Dominion {result.DominionFleetPower:0.00} | Collective {result.CollectiveFleetPower:0.00}");
        output.WriteLine($"Agent ticks: {result.AgentTicks}");
        output.WriteLine($"Decisions evaluated: {result.DecisionsEvaluated}");
        output.WriteLine($"Actions emitted: {result.ActionsEmitted} (Dominion {result.DominionActions}, Collective {result.CollectiveActions})");
        output.WriteLine($"Events delivered: {result.EventsDelivered} (Dominion {result.DominionEvents}, Collective {result.CollectiveEvents})");
        output.WriteLine($"Damage events: {result.DamageEvents}");
        output.WriteLine($"Repair events: {result.RepairEvents}");
        output.WriteLine($"Elapsed wall clock: {result.ElapsedWallClock.TotalMilliseconds:0.00} ms");
        output.WriteLine($"Measured simulation time: {result.MeasuredSimulationTime.TotalMilliseconds:0.00} ms");
        output.WriteLine($"Primary score AgentTicksPerSecond: {result.AgentTicksPerSecond:0.00}");
        output.WriteLine($"DecisionsPerSecond: {result.DecisionsPerSecond:0.00}");
        output.WriteLine($"ActionsPerSecond: {result.ActionsPerSecond:0.00}");
        output.WriteLine($"EventsPerSecond: {result.EventsPerSecond:0.00}");
        output.WriteLine("Phase timings:");
        foreach (var phase in result.PhaseTimings)
            output.WriteLine(string.Create(CultureInfo.InvariantCulture, $"  {phase.Name}: {phase.PercentOfMeasuredRuntime:0.0}% ({phase.Elapsed.TotalMilliseconds:0.00} ms)"));
        output.WriteLine(result.HotPathSummary);
        output.WriteLine("Diagnostics:");
        output.WriteLine($"  Sensor mode: {result.SensorMode}");
        output.WriteLine(string.Create(CultureInfo.InvariantCulture, $"  Spatial cell size: {result.SpatialCellSize:0.##}"));
        output.WriteLine($"  Sensor pairs checked: {result.SensorPairsChecked}");
        output.WriteLine($"  Broad equivalent pairs: {result.BroadSensorPairsEquivalent}");
        output.WriteLine($"  Spatial candidate pairs: {result.SpatialCandidatePairs}");
        output.WriteLine($"  Pairs skipped by grid: {result.SpatialPairsSkippedByGrid}");
        output.WriteLine($"  Spatial max cells used: {result.SpatialMaxCellsUsed}");
        output.WriteLine($"  Spatial cell queries: {result.SpatialCellQueries}");
        output.WriteLine("Sensor cadence diagnostics:");
        output.WriteLine($"  Dynamic cadence: {(result.DynamicSensorCadenceEnabled ? "enabled" : "disabled")}");
        output.WriteLine($"  Refreshes performed: {result.SensorRefreshesPerformed}");
        output.WriteLine($"  Refreshes skipped: {result.SensorRefreshesSkipped}");
        output.WriteLine(string.Create(CultureInfo.InvariantCulture, $"  Skip rate: {result.SensorRefreshSkipRate:P1}"));
        output.WriteLine($"  Stale summary uses: {result.StaleTacticalSummaryUses}");
        output.WriteLine($"  Forced refreshes: {result.ForcedSensorRefreshes} (damage {result.DamageForcedRefreshes}, event {result.EventForcedRefreshes}, target {result.TargetInvalidationRefreshes})");
        output.WriteLine(string.Create(CultureInfo.InvariantCulture, $"  Average cadence: {result.AverageSensorCadenceTicks:0.00} ticks"));
        output.WriteLine($"  Cadence selections: immediate {result.ImmediateCadenceSelections}, near {result.NearCadenceSelections}, sensor {result.SensorBandCadenceSelections}, idle {result.IdleCadenceSelections}");
        output.WriteLine("Decision diagnostics:");
        output.WriteLine($"  Agent tick calls: {result.AgentTickCalls}");
        output.WriteLine($"  HFSM ticks: {result.HfsmTicks}");
        output.WriteLine($"  Decide steps: {result.DecideSteps}");
        output.WriteLine($"  Utility options evaluated: {result.UtilityOptionsEvaluated}");
        output.WriteLine($"  Utility options selected: {result.UtilityOptionsSelected}");
        output.WriteLine(string.Create(CultureInfo.InvariantCulture, $"  Utility options / agent tick: {result.UtilityOptionsPerAgentTick:0.00}"));
        output.WriteLine(string.Create(CultureInfo.InvariantCulture, $"  Decisions / agent tick: {result.DecisionsPerAgentTick:0.00}"));
        output.WriteLine(string.Create(CultureInfo.InvariantCulture, $"  Actions / agent tick: {result.ActionsPerAgentTick:0.00}"));
        output.WriteLine($"  Blackboard reads/writes: {result.DecisionBlackboardReads}/{result.DecisionBlackboardWrites}");
        output.WriteLine($"  Parallel agent ticks/tasks/faults/staged: {result.ParallelAgentTicks}/{result.ParallelDecisionTasksScheduled}/{result.ParallelDecisionFaults}/{result.ParallelLocalActionsStaged}");
        output.WriteLine($"  Action states entered: {result.ActionStatesEntered}");
        output.WriteLine("  Action distribution:");
        output.WriteLine($"    FocusFire: {result.FocusFireActionsEmitted}");
        output.WriteLine($"    Retreat: {result.RetreatActionsEmitted}");
        output.WriteLine($"    Repair: {result.RepairActionsEmitted}");
        output.WriteLine($"    Advance: {result.AdvanceActionsEmitted}");
        output.WriteLine($"    LaunchDrone: {result.LaunchDroneActionsEmitted}");
        output.WriteLine($"    Regenerate: {result.RegenerateActionsEmitted}");
        output.WriteLine($"    HoldFormation/ScreenHighValue: {result.HoldFormationActionsEmitted}");
        output.WriteLine($"    Idle: {result.IdleActionsEmitted}");
        output.WriteLine("Allocation diagnostics:");
        output.WriteLine($"  Allocated bytes: {result.AllocatedBytes}");
        output.WriteLine(string.Create(CultureInfo.InvariantCulture, $"  Bytes / agent tick: {result.BytesPerAgentTick:0.00}"));
        output.WriteLine(string.Create(CultureInfo.InvariantCulture, $"  Bytes / decision: {result.BytesPerDecision:0.00}"));
        output.WriteLine($"  GC collections: Gen0 {result.Gen0Collections} / Gen1 {result.Gen1Collections} / Gen2 {result.Gen2Collections}");
        output.WriteLine("  Allocation source: GC.GetAllocatedBytesForCurrentThread covers this benchmark's single-threaded measured run and excludes final report formatting.");
        output.WriteLine("Event diagnostics:");
        output.WriteLine($"  Mailbox sent/delivered: {result.MailboxEventsSent}/{result.MailboxEventsDelivered}");
        output.WriteLine(string.Create(CultureInfo.InvariantCulture, $"  Events / agent tick: {result.EventsPerAgentTick:0.00}"));
        output.WriteLine(string.Create(CultureInfo.InvariantCulture, $"  Events / action: {result.EventsPerAction:0.00}"));
        output.WriteLine($"  TargetSpotted: {result.TargetSpottedEvents}");
        output.WriteLine($"  RepairRequested: {result.RepairRequestedEvents}");
        output.WriteLine($"  CommandFocusOrder: {result.CommandFocusOrderEvents}");
        output.WriteLine($"  ShipDestroyed: {result.ShipDestroyedEvents}");
        output.WriteLine($"  SynapseLost: {result.SynapseLostEvents}");
        output.WriteLine($"  AllyUnderFire: {result.AllyUnderFireEvents}");
        output.WriteLine("Action diagnostics:");
        output.WriteLine($"  Max actions/tick: {result.MaxActionsInTick}");
        output.WriteLine(string.Create(CultureInfo.InvariantCulture, $"  Avg actions/tick: {result.AverageActionsPerTick:0.00}"));
        output.WriteLine($"  Action sort batches: {result.ActionSortBatches}");
        output.WriteLine($"  Actions sorted: {result.ActionsSorted}");
        output.WriteLine("Tactical band diagnostics:");
        output.WriteLine($"  Relevant enemy contacts: {result.RelevantEnemyContacts}");
        output.WriteLine($"  Relevant ally contacts: {result.RelevantAllyContacts}");
        output.WriteLine($"  Ignored out-of-range contacts: {result.IgnoredOutOfRangeContacts}");
        output.WriteLine($"  Immediate/Near/Sensor band contacts: {result.ImmediateThreatContacts}/{result.NearContacts}/{result.SensorBandContacts}");
        output.WriteLine(string.Create(CultureInfo.InvariantCulture, $"  Relevant contacts per agent tick: {result.RelevantContactsPerAgentTick:0.00}"));
        output.WriteLine(string.Create(CultureInfo.InvariantCulture, $"  Ignored contacts per sensor pair: {result.IgnoredContactsPerSensorPair:0.00}"));
        output.WriteLine($"  Total blackboard reads/writes: {result.BlackboardReads}/{result.BlackboardWrites}");
        output.WriteLine($"  Sensor blackboard writes: {result.SensorBlackboardWrites}");
        output.WriteLine($"  Checkpoints written: {result.CheckpointsWritten}");
        output.WriteLine($"Determinism hash: {result.DeterminismHash}");
        output.WriteLine("No rendering, GPU, LLM, network, or provider calls are used.");
    }
}
